package methods;

public class Maths {
	public static double findArea(int r) {
		return Math.PI * (r * r);
	}
	public static int add(int a, int b) {
		return a + b;
	}
	
	public static int subtract(int a, int b) {
		return a -b;
		
	}
	public static int multiple(int a, int b) {
		return a * b;
		
	}
	public static int divide(int a, int b) {
		return a / b;
		
	}
}
